#!/usr/bin/env node
/**
 * 从 models.dev/api.json 同步「支持图片输入」的模型列表，生成本地快照供 isVisionModel 使用。
 *
 * 更新供应商映射：scripts/provider-modelsdev.manifest.json → pnpm sync
 */
import { createHash } from 'node:crypto'
import { mkdirSync, readFileSync, writeFileSync, existsSync } from 'node:fs'
import { dirname, join } from 'node:path'
import { fileURLToPath } from 'node:url'

const root = join(dirname(fileURLToPath(import.meta.url)), '..')
const manifestPath = join(root, 'scripts/provider-modelsdev.manifest.json')
const snapshotPath = join(root, 'packages/shared/src/data/vision-models.snapshot.generated.ts')
const stampPath = join(root, 'packages/shared/src/data/.vision-models-sync-stamp.json')

const MODELS_DEV_API = 'https://models.dev/api.json'
const checkOnly = process.argv.includes('--check')
const force = process.argv.includes('--force')

function md5(text) {
  return createHash('md5').update(text).digest('hex')
}

function readManifest() {
  return JSON.parse(readFileSync(manifestPath, 'utf8'))
}

function isChatVisionModel(modelId, model, excludePatterns) {
  const inputs = model?.modalities?.input
  if (!Array.isArray(inputs) || !inputs.includes('image')) {
    return false
  }
  for (const pattern of excludePatterns) {
    if (new RegExp(pattern, 'i').test(modelId)) {
      return false
    }
  }
  return true
}

function collectVisionModelsForProvider(api, modelsDevIds, excludePatterns) {
  const modelIds = new Set()
  for (const devId of modelsDevIds) {
    const provider = api[devId]
    if (!provider?.models) continue
    for (const [modelId, model] of Object.entries(provider.models)) {
      if (isChatVisionModel(modelId, model, excludePatterns)) {
        modelIds.add(modelId)
      }
    }
  }
  return [...modelIds].sort()
}

function buildSnapshotTs(payload) {
  return `/* eslint-disable */
/**
 * AUTO-GENERATED by scripts/sync-vision-models.mjs — do not edit by hand.
 * Source: https://models.dev/api.json + scripts/provider-modelsdev.manifest.json
 */
export const VISION_MODELS_SNAPSHOT = ${JSON.stringify(payload, null, 2)} as const

export type VisionModelsSnapshot = typeof VISION_MODELS_SNAPSHOT
`
}

async function syncVisionModels() {
  const manifest = readManifest()
  const manifestHash = md5(readFileSync(manifestPath, 'utf8'))
  const stamp = existsSync(stampPath) ? JSON.parse(readFileSync(stampPath, 'utf8')) : null

  if (!force && stamp?.manifestMd5 === manifestHash && existsSync(snapshotPath)) {
    console.log('[sync-vision-models] Up to date (manifest unchanged).')
    return
  }

  const response = await fetch(MODELS_DEV_API, { signal: AbortSignal.timeout(60_000) })
  if (!response.ok) {
    throw new Error(`models.dev returned HTTP ${response.status}`)
  }
  const api = await response.json()

  const byProvider = {}
  const globalModelIds = new Set()
  let providerCount = 0

  for (const entry of manifest.providers) {
    const visionModels = collectVisionModelsForProvider(
      api,
      entry.modelsDevIds,
      manifest.excludeModelIdPatterns ?? []
    )
    byProvider[entry.id] = visionModels
    providerCount += visionModels.length
    for (const id of visionModels) {
      globalModelIds.add(id)
    }
    console.log(
      `[sync-vision-models] ${entry.id} ← [${entry.modelsDevIds.join(', ')}]: ${visionModels.length} vision models`
    )
  }

  const payload = {
    syncedAt: new Date().toISOString(),
    source: MODELS_DEV_API,
    byProvider,
    byModelId: [...globalModelIds].sort()
  }

  mkdirSync(dirname(snapshotPath), { recursive: true })
  writeFileSync(snapshotPath, buildSnapshotTs(payload), 'utf8')
  writeFileSync(
    stampPath,
    `${JSON.stringify(
      {
        manifestMd5: manifestHash,
        syncedAt: payload.syncedAt,
        providerEntries: manifest.providers.length,
        uniqueVisionModels: globalModelIds.size,
        providerVisionRows: providerCount
      },
      null,
      2
    )}\n`,
    'utf8'
  )

  console.log(
    `[sync-vision-models] Wrote snapshot: ${globalModelIds.size} unique models across ${manifest.providers.length} providers.`
  )
}

function checkVisionModels() {
  const manifestHash = md5(readFileSync(manifestPath, 'utf8'))
  const stamp = existsSync(stampPath) ? JSON.parse(readFileSync(stampPath, 'utf8')) : null

  if (!stamp || stamp.manifestMd5 !== manifestHash) {
    console.error('[sync-vision-models] Snapshot out of date. Run: pnpm sync')
    process.exit(1)
  }
  if (!existsSync(snapshotPath)) {
    console.error('[sync-vision-models] Missing generated snapshot. Run: pnpm sync')
    process.exit(1)
  }
  console.log('[sync-vision-models] OK')
}

if (checkOnly) {
  checkVisionModels()
} else {
  await syncVisionModels()
}
