#!/usr/bin/env node
/**
 * 从 LobeHub（@lobehub/icons / lobe-icons static-svg）下载供应商图标到本地，
 * 并生成 provider-icon-registry.generated.ts（桌面 URL + RN 组件共用）。
 *
 * 更新映射：scripts/provider-icon-lobe.manifest.json → pnpm sync
 */
import { createHash } from 'node:crypto'
import { mkdirSync, readFileSync, writeFileSync, existsSync } from 'node:fs'
import { dirname, join } from 'node:path'
import { fileURLToPath } from 'node:url'
import { getLobeIconCDN } from '@lobehub/icons/es/features/getLobeIconCDN/index.js'

const root = join(dirname(fileURLToPath(import.meta.url)), '..')
const manifestPath = join(root, 'scripts/provider-icon-lobe.manifest.json')
const iconDir = join(root, 'packages/ui/src/assets/ai_provider_icon')
const registryPath = join(root, 'packages/ui/src/utils/provider-icon-registry.generated.ts')
const stampPath = join(iconDir, '.lobe-icon-sync-stamp.json')

const checkOnly = process.argv.includes('--check')
const force = process.argv.includes('--force')

function md5(text) {
  return createHash('md5').update(text).digest('hex')
}

function readManifest() {
  const raw = JSON.parse(readFileSync(manifestPath, 'utf8'))
  return raw.providers
}

function lobeSvgUrl(lobeId, type) {
  return getLobeIconCDN(lobeId, { format: 'svg', type, cdn: 'github' })
}

async function fetchSvg(url) {
  const response = await fetch(url, { signal: AbortSignal.timeout(30_000) })
  if (!response.ok) return null
  const text = await response.text()
  if (!text.includes('<svg')) return null
  return text
}

function toImportVar(id, variant) {
  const safe = id.replace(/[^a-zA-Z0-9]/g, '_')
  return `${safe}_${variant}`
}

function buildRegistryTs(entries) {
  const imports = []
  const pairs = []

  for (const { id, monoFile, colorFile, monoOnly } of entries) {
    const monoVar = toImportVar(id, 'mono')
    const colorVar = toImportVar(id, 'color')
    imports.push(`import ${monoVar} from '../assets/ai_provider_icon/${monoFile}'`)
    if (monoOnly) {
      pairs.push(`  ${JSON.stringify(id)}: { light: ${monoVar}, dark: ${monoVar} }`)
    } else {
      imports.push(`import ${colorVar} from '../assets/ai_provider_icon/${colorFile}'`)
      pairs.push(`  ${JSON.stringify(id)}: { light: ${colorVar}, dark: ${monoVar} }`)
    }
  }

  return `/* eslint-disable */
/**
 * AUTO-GENERATED by scripts/sync-provider-icons.mjs — do not edit by hand.
 * Source: scripts/provider-icon-lobe.manifest.json + LobeHub static-svg CDN
 */
${imports.join('\n')}

export const PROVIDER_ICON_REGISTRY = {
${pairs.join(',\n')}
} as const

export type ProviderIconRegistryId = keyof typeof PROVIDER_ICON_REGISTRY

export const PROVIDER_ICON_IDS = Object.keys(PROVIDER_ICON_REGISTRY) as ProviderIconRegistryId[]
`
}

async function syncIcons() {
  mkdirSync(iconDir, { recursive: true })
  const providers = readManifest()
  const manifestHash = md5(readFileSync(manifestPath, 'utf8'))
  const stamp = existsSync(stampPath) ? JSON.parse(readFileSync(stampPath, 'utf8')) : null

  if (!force && stamp?.manifestMd5 === manifestHash && existsSync(registryPath)) {
    const missing = providers.some((p) => !existsSync(join(iconDir, `${p.id}.svg`)))
    if (!missing) {
      console.log('[sync-provider-icons] Up to date (manifest unchanged).')
      return
    }
  }

  const registryEntries = []
  const failures = []

  for (const provider of providers) {
    const { id, lobeId, monoOnly = false } = provider
    const monoUrl = lobeSvgUrl(lobeId, 'mono')
    const colorUrl = monoOnly ? null : lobeSvgUrl(lobeId, 'color')

    const monoSvg = await fetchSvg(monoUrl)
    if (!monoSvg) {
      failures.push(`${id}: mono not found at ${monoUrl}`)
      continue
    }

    const monoFile = `${id}.svg`
    writeFileSync(join(iconDir, monoFile), monoSvg, 'utf8')

    let colorFile = monoFile
    if (!monoOnly && colorUrl) {
      const colorSvg = (await fetchSvg(colorUrl)) ?? monoSvg
      colorFile = `${id}-color.svg`
      writeFileSync(join(iconDir, colorFile), colorSvg, 'utf8')
    }

    registryEntries.push({ id, monoFile, colorFile, monoOnly })
    console.log(`[sync-provider-icons] ${id} ← lobe:${lobeId}`)
  }

  if (failures.length > 0) {
    console.error('[sync-provider-icons] Failed downloads:\n' + failures.join('\n'))
    process.exit(1)
  }

  writeFileSync(registryPath, buildRegistryTs(registryEntries), 'utf8')
  writeFileSync(
    stampPath,
    `${JSON.stringify({ manifestMd5: manifestHash, syncedAt: new Date().toISOString(), count: registryEntries.length }, null, 2)}\n`,
    'utf8'
  )
  console.log(`[sync-provider-icons] Wrote ${registryEntries.length} icons + registry.`)
}

async function checkIcons() {
  const providers = readManifest()
  const manifestHash = md5(readFileSync(manifestPath, 'utf8'))
  const stamp = existsSync(stampPath) ? JSON.parse(readFileSync(stampPath, 'utf8')) : null

  if (!stamp || stamp.manifestMd5 !== manifestHash) {
    console.error('[sync-provider-icons] Icons out of date. Run: pnpm sync')
    process.exit(1)
  }

  for (const { id } of providers) {
    if (!existsSync(join(iconDir, `${id}.svg`))) {
      console.error(`[sync-provider-icons] Missing ${id}.svg — run: pnpm sync`)
      process.exit(1)
    }
  }

  if (!existsSync(registryPath)) {
    console.error('[sync-provider-icons] Missing generated registry — run: pnpm sync')
    process.exit(1)
  }

  console.log('[sync-provider-icons] OK')
}

if (checkOnly) {
  await checkIcons()
} else {
  await syncIcons()
}
